function visuals = createVisualStimuli(screen)

visuals.window = screen.window;

% text (instructions)
visuals.instrSize = 15; %25 CHANGED to fit things on screen
visuals.instrText = 'Geben Sie an, auf welcher Seite sich das Quadrat oeffnet. \n\n\n\n Warten Sie, bis es losgeht.';
visuals.abortText = 'Abbruch';
visuals.waitText = 'Bitte warten...';
visuals.pressText = 'Ende: Bitte druecken Sie jetzt eine beliebige Taste, um die Messung zu beenden.';
visuals.endText = 'Vielen Dank!';

% fixation square
visuals.fixSize = 30;                                                       % size of square side in pixels
visuals.fixWidth = 2;
visuals.fixCol = screen.white;
visuals.fixRect = [0 0 visuals.fixSize visuals.fixSize];
visuals.fixCoords = CenterRectOnPointd(visuals.fixRect, screen.xCenter, screen.yCenter);

% openings
visuals.openWidth = 5;
visuals.openCol = screen.gray;
visuals.openDist = visuals.fixSize - visuals.fixWidth;
visuals.openLeftCoords = CenterRectOnPointd(visuals.fixRect, screen.xCenter - visuals.openDist, screen.yCenter);
visuals.openRightCoords = CenterRectOnPointd(visuals.fixRect, screen.xCenter + visuals.openDist, screen.yCenter);

% text (training)
visuals.instrSize = 18; % 30; CHANGED
visuals.trainingStart = 'Druecken Sie eine beliebige Taste, um zu starten ...';
visuals.trainingContinue = 'Druecken Sie eine beliebige Taste, um fortzufahren ...';
visuals.trainingLeftpress = 'Druecken Sie jetzt die linke Taste.';
visuals.trainingRightpress = 'Druecken Sie jetzt die rechte Taste.';
visuals.trainingEnd = ['Die Uebung ist nun zu Ende. \n\n\n' ...
    'Sollten Sie noch Fragen haben, stellen Sie diese bitte \n\n' ...
    'nun dem Versuchsleiter. Viel Erfolg bei der Aufgabe!'];

visuals.training00 = ['Herzlich Willkommen zum Uebungsteil. \n\n\n\n' ...
    'Im Folgenden wird Ihnen die naechste Aufgabe erklaert. \n\n' ...
    'Sollten Sie nach der Uebung noch Fragen haben, \n\n zoegern Sie bitte nicht, diese zu stellen.'];

visuals.training01 = ['In der Mitte des Bildschirms werden Sie immer dieses Quadrat sehen.  \n\n' ...
    'Von Zeit zu Zeit wird sich das Quadrat auf einer Seite kurz oeffnen.  \n\n' ...
    'Ihre Aufgabe ist es, mit einem Tastendruck auf dieses Oeffnen zu reagieren.'];

visuals.training02 = 'Wenn sich das Quadrat links oeffnet, so wie hier, \n\n druecken Sie bitte die linke Taste.';
visuals.training03 = 'Wenn sich das Quadrat rechts oeffnet, so wie hier, \n\n druecken Sie bitte die rechte Taste.';
visuals.training04 = ['Die Oeffnung ist jeweils nur sehr kurz sichtbar.  \n\n' ...
    'Beim naechsten Tastendruck sehen Sie ein paar Oeffnungen in Echtzeit:'];

visuals.training05 = ['Das Quadrat hat sich eben einmal nach links \n\n und zweimal nach rechts geoeffnet.  \n\n\n' ... 
    'Um das Oeffnen nicht zu verpassen, muessen Sie Ihren Blick waehrend  \n\n' ...
    'des gesamten Experiments stets auf das Quadrat halten. Es kann sein, \n\n' ...
    'dass sich manchmal sehr lange nichts tut, oder dass sich das Quadrat \n\n' ...
    'manchmal mehrmals schnell hintereinander oeffnet.'];

visuals.training06 = ['Bitte reagieren Sie so schnell und fehlerfrei wie moeglich \n\n' ...
    'auf das Oeffnen des Quadrats.  \n\n\n' ... 
    'Je schneller Sie reagieren, und desto weniger Fehler Sie machen,  \n\n' ...
    'desto mehr Bonus-Geld erhalten Sie am Ende des Experiments.'];

visuals.training07 = ['Nun folgt eine kleine Uebung. \n\n\n' ...
    'Das Quadrat wird sich einige Male oeffnen. \n\n' ... 
    'Bitte druecken Sie die linke Taste, wenn es sich links oeffnet,  \n\n' ...
    'und die rechte Taste, wenn es sich rechts oeffnet.'];

visuals.training08 = ['Ende der Uebung. \n\n\n' ...
    'Sollten Sie noch Fragen zur Aufgabe haben, stellen Sie diese \n\n' ...
    'bitte nach dem Ende dieser Messung dem/der Versuchsleiter/in. \n\n\n' ...
    'Wichtig: Im eigentlichen Experiment werden Sie im Hintergrund Toene hoeren, \n\n' ... 
    'waehrend Sie das Quadrat betrachten. Um sicher zu gehen, dass Sie die Toene \n\n' ...
    'gut hoeren koennen, \n\n spielen wir Ihnen die Toene jetzt einmal vor.'];
    
visuals.tone01 = ['Tontest. \n\n' ...
    'Sie sollten jetzt 10 kurze Toene hoeren.' ];

visuals.tone02 = ['Ende des Tontests. \n\n\n' ...
    'Falls Sie die Toene nicht hoeren konnten, sagen Sie dies bitte unbedingt \n\n' ...
    'dem/der Versuchsleiter/in nach dem Ende dieser Uebung! \n\n\n' ...
    'Wichtig: Im eigentlichen Experiment koennen Sie die Toene ignorieren. \n\n' ...
    'Sie stehen in keinem Zusammenhang zu dem Oeffnen des Quadrats. \n\n' ...
    'Achten Sie nur auf das Quadrat und reagieren Sie so schnell Sie koennen.'];

end
